#include "ft_math_utils.h"

t_vec3	ft_vec3_reflect(t_vec3 v, t_vec3 n)
{
	return (ft_vec3_sub(v, ft_vec3_mult(n, 2.0 * ft_vec3_dot(v, n))));
}
